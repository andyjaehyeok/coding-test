//두 수의 차 구하기
function minus(num1: number, num2: number) {
  return num1 - num2;
}

//두 수의 합 구하기
function plus(num1: number, num2: number) {
  return num1 + num2;
}

//두 수의 곱 구하기
function times(num1: number, num2: number) {
  return num1 * num2;
}

//몫 구하기
function divide(num1: number, num2: number) {
  return Math.floor(num1 / num2);
}
